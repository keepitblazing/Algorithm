// 문제 설명

// 효진이는 멀리 뛰기를 연습하고 있습니다. 효진이는 한번에 1칸, 또는 2칸을 뛸 수 있습니다. 칸이 총 4개 있을 때, 효진이는
// (1칸, 1칸, 1칸, 1칸)
// (1칸, 2칸, 1칸)
// (1칸, 1칸, 2칸)
// (2칸, 1칸, 1칸)
// (2칸, 2칸)
// 의 5가지 방법으로 맨 끝 칸에 도달할 수 있습니다. 멀리뛰기에 사용될 칸의 수 n이 주어질 때, 효진이가 끝에 도달하는 방법이 몇 가지인지 알아내, 여기에 1234567를 나눈 나머지를 리턴하는 함수, solution을 완성하세요. 예를 들어 4가 입력된다면, 5를 return하면 됩니다.

function solution(n) {
  // n이 1이나 2일 때는 각각 1, 2를 반환한다.
  if (n === 1) return 1;
  if (n === 2) return 2;
  // n이 3 이상일 때는 피보나치 수열을 이용한다.
  const mod = 1234567;
  let prev = 1;
  let curr = 2;
  // n이 3일 때부터 피보나치 수열을 이용하여 계산한다.
  for (let i = 3; i <= n; i++) {
    // 피보나치 수열의 특성상 1234567로 나눈 나머지를 구한다.
    let temp = (prev + curr) % mod;
    // 다음 계산을 위해 prev와 curr을 갱신한다.
    prev = curr;
    curr = temp;
  }

  return curr;
}
